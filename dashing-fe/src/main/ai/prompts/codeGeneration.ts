/**
 * Validate and clean generated code
 */
export function validateGeneratedCode(code: string, framework: Framework): {
  valid: boolean;
  cleaned: string;
  issues: string[];
} {
  const issues: string[] = [];
  let cleaned = code;

  // Remove markdown code blocks if present
  cleaned = cleaned.replace(/```(?:typescript|javascript|ts|js)?\n?/g, '');
  cleaned = cleaned.replace(/```\n?/g, '');
  cleaned = cleaned.trim();

  // Check for required imports/structure
  if (framework === 'playwright') {
    if (!cleaned.includes('test') && !cleaned.includes('expect')) {
      issues.push('Missing Playwright test imports');
    }
    if (!cleaned.includes('page')) {
      issues.push('No page interactions found');
    }
  } else {
    if (!cleaned.includes('describe') && !cleaned.includes('it')) {
      issues.push('Missing Cypress test structure');
    }
    if (!cleaned.includes('cy.')) {
      issues.push('No Cypress commands found');
    }
  }

  return {
    valid: issues.length === 0,
    cleaned,
    issues,
  };
}
